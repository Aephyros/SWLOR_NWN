
CREATE TABLE DataPackages(
	DataPackageID INT NOT NULL PRIMARY KEY IDENTITY,
	DateFound DATETIME2	NOT NULL DEFAULT GETUTCDATE(),
	DateExported DATETIME2 NOT NULL,
	FileName NVARCHAR(MAX) NOT NULL,
	PackageName NVARCHAR(64) NOT NULL,
	Checksum NCHAR(40) NOT NULL,
	Content NVARCHAR(MAX) NOT NULL,
	ImportedSuccessfully BIT NOT NULL,
	ErrorMessage NVARCHAR(MAX) NOT NULL DEFAULT ''
)